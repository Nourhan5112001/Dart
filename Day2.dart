void calculateTotal(Map<String, double> cart, [double discount = 0.0]) {
  double total = cart.values.fold(0, (sum, item) => sum + item);
  double discountedTotal = total * (1 - discount);

  print("Cart items:");
  cart.forEach((item, price) {
    print(" - $item: \$${price.toStringAsFixed(2)}");
  });

  print("\nTotal before discount: \$${total.toStringAsFixed(2)}");

  if (discount > 0 && discount < 1) {
    print("Discount applied: ${discount * 100}%");
    print("Total after discount: \$${discountedTotal.toStringAsFixed(2)}");
  } else {
    print("No discount applied.");
  }
}

